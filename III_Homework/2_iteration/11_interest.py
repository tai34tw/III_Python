'''11.	迴圈敘述的練習-interest
錢精打以10%單利投資100000元，郝細算則以5%複利投資100000元。計算多少年後郝細算的投資可以超過錢精打，並將此時兩人錢數印出。(27年後)
提示：單利公式：P(1+r*n)    複利公式：P(1+r)n
P：本金，r：利率，n：多少年
'''


yr = 1
while True:
    m1 = 100000*(1 + 0.1 * yr)
    m2 = 100000*(1 + 0.05) ** yr
    if m2 > m1:
        print(yr, m1, m2)
        break
    else:
        yr += 1




